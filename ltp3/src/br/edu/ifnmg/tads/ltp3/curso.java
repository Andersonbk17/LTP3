package br.edu.ifnmg.tads.ltp3;

/**
 *
 * @author aluno
 */
public class curso {
    
    private int id;
    private String nome;

    public curso() {
        id = 0;
        nome = "";
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
    
  
    
    
}
